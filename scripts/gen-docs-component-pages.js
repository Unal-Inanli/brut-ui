#!/usr/bin/env node
// Generates one Markdown page per preview/components-<name>.html and
// emits a sidebar fragment for VitePress. Idempotent — safe to re-run.
//
// Read preview file → extract title → write docs-site/components/<name>.md
// with iframe to /preview/components-<name>.html. The preview file is
// served by VitePress's `public/` mechanism (we copy preview/ into
// docs-site/public/preview/ at build time — see .vitepress/config.ts).

import { readFileSync, writeFileSync, readdirSync, mkdirSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const previewDir = join(root, 'preview')
const outDir = join(root, 'docs-site', 'components')
const sidebarOut = join(root, 'docs-site', '.vitepress', 'components-sidebar.ts')

mkdirSync(outDir, { recursive: true })

const files = readdirSync(previewDir)
  .filter(f => f.startsWith('components-') && f.endsWith('.html'))
  .sort()

const pages = []

for (const file of files) {
  const slug = file.replace(/^components-/, '').replace(/\.html$/, '')
  const html = readFileSync(join(previewDir, file), 'utf8')
  const titleMatch = html.match(/<title>([^<]+)<\/title>/i)
  const rawTitle = titleMatch ? titleMatch[1].trim() : slug
  // "BRUT — Buttons preview" → "Buttons"
  const title = rawTitle
    .replace(/^BRUT\s*[—–-]\s*/i, '')
    .replace(/\s*[—–-]?\s*preview$/i, '')
    .trim() || slug

  // Use a relative URL (single '../') so VitePress's base path is honored
  // in production (e.g. /brut-ui/components/${slug} → /brut-ui/preview/...).
  const md = `---
title: ${title}
---

# ${title}

<iframe
  class="brut-preview-frame"
  src="../preview/components-${slug}.html"
  loading="lazy"
  style="height: 720px;"
  title="${title} preview"
></iframe>

::: tip Markup source
The preview above is served from \`preview/components-${slug}.html\` —
the canonical fixture for this component. Copy markup directly from
the source to use it in your own page.
:::

## Where to look

- **Source CSS:** [\`src/components.css\`](https://github.com/Unal-Inanli/brut-ui/blob/main/src/components.css) (search for \`.brut-${slug}\`)
- **Manifest entry:** [\`dist/components.json\`](https://github.com/Unal-Inanli/brut-ui/blob/main/dist/components.json) (filter by \`name\`)
- **Reference:** [Manifest schema](/reference/manifest)
`
  writeFileSync(join(outDir, `${slug}.md`), md)
  pages.push({ slug, title })
}

const sidebarTs = `// AUTO-GENERATED by scripts/gen-docs-component-pages.js — do not edit by hand.
// Run \`node scripts/gen-docs-component-pages.js\` after changing preview pages.

export const componentsSidebar = [
  { text: 'Overview', link: '/components/' },
${pages
  .map(p => `  { text: ${JSON.stringify(p.title)}, link: '/components/${p.slug}' },`)
  .join('\n')}
]
`
writeFileSync(sidebarOut, sidebarTs)

console.log(`Wrote ${pages.length} component pages and sidebar fragment.`)
